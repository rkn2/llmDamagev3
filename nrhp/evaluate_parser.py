"""Validation harness for the NRHP parser (see nrhp/PROTOCOL.md for the metric).

Runs five independent check groups against nrhp/nrhp_inventory.json and writes
nrhp/validation_report.json. GROUND_TRUTH below was hand-verified against the raw
extracted PDF text during recon (each entry was read in full) — it is the parser's
spot-check oracle and must never be generated by the parser itself.
"""

